<h1>O Estrangeiro</h1>

<p>O Estrangeiro” faz parte do chamado Ciclo dos Absurdos, do romancista francês Albert Camus, composto pelo romance “O Estrangeiro”, 
    o ensaio “O Mito de Sísifo” e a peça teatral “Calígula”.
     Esta obra de “alma existencialista” pode ser apreciada em apenas algumas horas!!!</p>
     <h1>A Metamorfose</h1>

     <p>Esta é uma das obras mais conhecidas de Kafka e, com certeza, uma das mais perturbadoras da literatura moderna! “A Metamorfose” é uma pequena novela que narra a história de um caixeiro viajante que acorda metamorfoseado em um inseto repugnante!
        Percursor da chamada “ficção absurda”, Kafka levou menos de um mês para escrever “A Metamorfose”, e você levará menos de um dia para apreciar esta leitura.</p>
<h1>Extraordinário</h1>  
<p>A emocionante história de Auggie não deixará você largar este livro até terminar de ler a última linha! “Extraordinário” é um sucesso da literatura infanto juvenil contemporânea, apresentando como plano de fundo o modo como as pessoas lidam com as diferenças e o preconceito. Imperdível! O livro foi adaptado para o cinema em 2017 com Jacob Tremblay (O Quarto de Jack) como Auggie.</p>
<h1>Vidas Secas</h1>
<p>Um dos maiores clássicos da literatura brasileira, “Vidas Secas”, do escritor Graciliano Ramos, relata as dificuldades sofridas por uma família de retirantes no sertão do Brasil no começo do século XX.
<p>
Ao longo da história dividida em 13 capítulos, acompanhamos pequenas passagens da vida de Fabiano, Sinhá Vitória e seus dois filhos. Outro personagem marcante neste livro é a cadela Baleia, adorada pelos filhos da família, mas que acaba adoecendo ao longo da história. A adaptação da novela em filme, dirigida por Nelson Pereira dos Santos, recebeu o prêmio OCIC no Festival de Cannes de 1964.</p>
